/**
  * @brief This function handles EXTI line[9:5] interrupts.
  */
void EXTI9_5_IRQHandler(void)
{
  /* USER CODE BEGIN EXTI9_5_IRQn 0 */
	if(EXTI->PR & EXTI_PR_PR9){
		worker(2);
	}
	if(EXTI->PR & EXTI_PR_PR8){
		worker(1);
	}
	if(EXTI->PR & EXTI_PR_PR6){
		worker(3);
	}
  /* USER CODE END EXTI9_5_IRQn 0 */
  HAL_GPIO_EXTI_IRQHandler(GPIO_PIN_6);
  HAL_GPIO_EXTI_IRQHandler(GPIO_PIN_8);
  HAL_GPIO_EXTI_IRQHandler(GPIO_PIN_9);
  /* USER CODE BEGIN EXTI9_5_IRQn 1 */

  /* USER CODE END EXTI9_5_IRQn 1 */
}

/**
  * @brief This function handles TIM1 update interrupt and TIM10 global interrupt.
  */
void TIM1_UP_TIM10_IRQHandler(void)
{
  /* USER CODE BEGIN TIM1_UP_TIM10_IRQn 0 */
	HAL_ADC_Start(&hadc1);																											//Start new ADC convertion
	int8_t TEMP = ((4096-HAL_ADC_GetValue(&hadc1))/33.03)-35;																		//Getting temperature value
	sprintf(TEMP_TX, "Temperature is %d degrees in Celsius\r\n", TEMP);																//Create string to send
	HAL_UART_Transmit(&huart3, (uint8_t *)TEMP_TX, strlen(TEMP_TX), 1000);															//Sending string over uart
  /* USER CODE END TIM1_UP_TIM10_IRQn 0 */
  HAL_TIM_IRQHandler(&htim1);
  /* USER CODE BEGIN TIM1_UP_TIM10_IRQn 1 */

  /* USER CODE END TIM1_UP_TIM10_IRQn 1 */
}

