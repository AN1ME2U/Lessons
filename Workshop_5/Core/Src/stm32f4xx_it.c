/* USER CODE BEGIN 1 */
void HAL_UART_RxCpltCallback (UART_HandleTypeDef * huart){
	if(huart == &huart3){
		worker(RX[0] - '0');																										//Convert char to int and call function
	}
}

void worker(uint8_t num){
	for(uint8_t i = 1; i < 5; i++){
		if(i == num){
			GPIOD->ODR ^= 1 << (i+11);																								//Toggle corresponding pin
			sprintf(TX,"LED %d is in state %d\r\n", i, (uint8_t)((GPIOD->ODR >> (i+11)) & 1));										//Create string to send
			HAL_UART_Transmit(&huart3, (uint8_t *)TX, sizeof(TX), 1000);															//Sending string over uart
		}
	}
	HAL_UART_Receive_IT(&huart3, (uint8_t *)RX,1);																					//Start next uart receive
}


